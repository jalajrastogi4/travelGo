const multer = require('multer');
const sharp = require('sharp');
const Tour = require('./../models/tourModel');
const catchAsync = require('./../utils/catchAsync');
const factory = require('./handlerFactory');
const AppError = require('./../utils/appError');

// before resizing it is better to save file in memory than on disk
const multerStorage = multer.memoryStorage();

// Specify the filter criteria for file uplaod : like only allow for image type files
const multerFilter = (req, file, cb) => {
  if (file.mimetype.startsWith('image')) {
    cb(null, true);
  } else {
    cb(new AppError('Not an image! Please upload only images', 400), false);
  }
};

// Add the storage and filter options to upload
const upload = multer({
  storage: multerStorage,
  fileFilter: multerFilter
});

// Middleware for multiple uploads (mixed) in tours
exports.uploadTourImages = upload.fields([
  // this will give: req.files
  { name: 'imageCover', maxCount: 1 },
  { name: 'images', maxCount: 3 }
]);

// in case of multiple files at same time (eg w/o imageCover) : upload.array('images', 5) - req.files
// for single file : upload.single('photo') : will give - req.file

// Middleware for resizing tour images
exports.resizeTourImages = catchAsync(async (req, res, next) => {
  console.log(req.files);

  if (!req.files.imageCover || !req.files.images) return next();

  // 1) Cover Image
  req.body.imageCover = `tour-${req.params.id}-${Date.now()}-cover.jpeg`; // putting imageCoverFilename on body
  await sharp(req.files.imageCover[0].buffer)
    .resize(2000, 1333)
    .toFormat('jpeg')
    .jpeg({ quality: 90 })
    .toFile(`public/img/tours/${req.body.imageCover}`);

  // 2) Images
  req.body.images = []; // creating empty array to store images on body

  // using foreach skipped the async function and moved to next()
  // used map to save the array of promises
  await Promise.all(
    req.files.images.map(async (file, i) => {
      const filename = `tour-${req.params.id}-${Date.now()}-${i + 1}.jpeg`;

      await sharp(file.buffer)
        .resize(2000, 1333)
        .toFormat('jpeg')
        .jpeg({ quality: 90 })
        .toFile(`public/img/tours/${filename}`);

      req.body.images.push(filename);
    })
  );

  console.log(req.body);
  next();
});

